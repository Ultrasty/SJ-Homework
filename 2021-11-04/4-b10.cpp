   /***************************************************************************
     函数名称：
     功    能：求num是不是base的幂
     输入参数：
     返 回 值：0 - 不是
               1 - 是
     说    明：函数名、形参、返回类型均不准动
   ***************************************************************************/
int is_power(int num, int base)
{
    if (num == 1) {
        return 1;
    }
    else if (num % base != 0) {
        return 0;
    }
    else {
        if (num == base) {
            return 1;
        }
        if (num > base) {
            return is_power(num / base, base);
        }
        if (num < base) {
            return 0;
        }
    }
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：完成输入、调用递归函数、输出
***************************************************************************/
int main()
{
    cout << "请输入整数num及基数base" << endl;
    bool mi;
    int num, base;
    cin >> num >> base;
    mi = is_power(num, base);
    if (mi == 1) {
        cout << num << "是" << base << "的幂";
    }
    if (mi == 0) {
        cout << num << "不是" << base << "的幂";
    }
    cout << endl;
}